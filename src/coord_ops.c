#include "../fdf.h"

int			validate_points(int *x, int *y, int *x1, int *y1)
{
	if (*x >= 0 && *x < WIDTH && *y >= 0 && *y < HEIGHT)
		return (1);
	if (*x1 >= 0 && *x1 < WIDTH && *y1 >= 0 && *y1 < HEIGHT)
		return (1);
	return (0);
}